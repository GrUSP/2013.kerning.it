	<div class="topic">
		<div class="title">Responsive Web Typography</div>
		<div class="abstract">
			<p>Responsive Web Design done correctly is based upon content rather than actual device resolutions, and so typography should adapt to both layout and sizing across content breakpoints. </p>
			<p>However, mere font sizing along the typographic scale based upon two-dimensional viewport @media queries is not enough. </p>
			<p>Apart from setting the correct balance of the black and the white in letterforms and texts, there are other important factors such as reading distance, information density, screen sharpness and device orientation that also influence the reading process. </p>
			<p>Learn to look at typography multidimensionally and enhance the overall responsive experience.</p>
